package BaiTapThem;

public class IfElse {
    public static void main(String[] args) {
        float diem = 7.2f;

        if (diem >= 8){
            System.out.println("Gioi");
        }else if (diem >= 6.5){
            System.out.println("Kha");
        }else if (diem >= 5){
            System.out.println("Trung binh");
        } else if (diem <5) {
            System.out.println("Yeu");
        }

    }
}
